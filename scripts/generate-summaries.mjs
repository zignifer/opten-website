#!/usr/bin/env node
// Speed/Phase B: emit src/content/models/_summaries.ts — a lightweight barrel of
// per-model intro strings (ru + en) for the /models hub grid + RelatedModels
// cards. The CLIENT build reads ONLY this (via index.client.ts) instead of the
// eager 62-file content glob, so the full model prose no longer ships in the
// entry chunk. Full content reaches a model page per-request via the
// #opten-model data-island (see scripts/prerender.mjs).
//
// MUST run BEFORE `vite build` (the client build imports _summaries.ts).
// Mirrors the generator pattern of scripts/build-models-registry.mjs.
//
//   node scripts/generate-summaries.mjs
//
// Source:  src/content/models/slugs.ts + each <slug>.ts (content.{ru,en}.intro)
// Target:  src/content/models/_summaries.ts (AUTO-GEN)

import { readFile, writeFile } from "node:fs/promises";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import esbuild from "esbuild";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const MODELS_DIR = join(ROOT, "src/content/models");
const OUT = join(MODELS_DIR, "_summaries.ts");

// Transpile a TS module (type-only imports erased) and import it via a data: URL
// — no temp files. Content files only `import type` from ./types, so transform
// (no bundling) is sufficient and resolves nothing at runtime.
async function loadTs(tsPath) {
  const src = await readFile(tsPath, "utf8");
  const { code } = await esbuild.transform(src, { loader: "ts", format: "esm" });
  const url = "data:text/javascript;base64," + Buffer.from(code, "utf8").toString("base64");
  return import(url);
}

const CYRILLIC = /[Ѐ-ӿ]/;

async function main() {
  const { MODEL_SLUGS_WITH_CONTENT } = await loadTs(join(MODELS_DIR, "slugs.ts"));
  if (!Array.isArray(MODEL_SLUGS_WITH_CONTENT) || MODEL_SLUGS_WITH_CONTENT.length === 0) {
    console.error("✗ generate-summaries: MODEL_SLUGS_WITH_CONTENT empty — slugs.ts changed?");
    process.exit(1);
  }

  const summaries = {};
  const cyrillicInEn = [];
  for (const slug of MODEL_SLUGS_WITH_CONTENT) {
    const mod = await loadTs(join(MODELS_DIR, `${slug}.ts`));
    const c = mod.content;
    if (!c || !c.ru || !c.en || typeof c.ru.intro !== "string" || typeof c.en.intro !== "string") {
      console.error(`✗ generate-summaries: ${slug}.ts missing content.{ru,en}.intro`);
      process.exit(1);
    }
    summaries[slug] = { ru: c.ru.intro, en: c.en.intro };
    if (CYRILLIC.test(c.en.intro)) cyrillicInEn.push(slug);
  }

  // EN-intro Cyrillic gate (same philosophy as verify-models-content.mjs's
  // EN-meta gate): an English intro with Cyrillic letters is a content bug.
  if (cyrillicInEn.length) {
    console.error(`✗ generate-summaries: Cyrillic found in EN intro of: ${cyrillicInEn.join(", ")}`);
    process.exit(1);
  }

  const header = `// AUTO-GENERATED by scripts/generate-summaries.mjs — do not edit by hand.
// Lightweight per-model intro strings for the /models hub + RelatedModels cards.
// Full model content lives in <slug>.ts and ships per-page via the #opten-model
// data-island (Speed/Phase B), NOT in the client entry chunk.
// Rerun \`node scripts/generate-summaries.mjs\` after content changes.

export interface ModelSummary {
  ru: string;
  en: string;
}

export const MODEL_SUMMARIES: Record<string, ModelSummary> = ${JSON.stringify(summaries, null, 2)};
`;

  await writeFile(OUT, header, "utf8");
  console.log(`✓ Wrote ${Object.keys(summaries).length} summaries → ${OUT.replace(ROOT, "")}`);
}

main().catch((err) => {
  console.error("✗ generate-summaries failed:", err);
  process.exit(1);
});
